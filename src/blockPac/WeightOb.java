package blockPac;

public class WeightOb {
	private int x;
	private int y;
	private int weight;
	public WeightOb(int x, int y, int weight) {
		this.x = x;
		this.y = y;
		this.weight = weight;
	}
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	public int getW() {
		return weight;
	}
	
}
